public class loop_concept {
    public static void main(String[] args) {
        // This is the concept related to for loop;
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
        // This is the concept about do while loop
        int i = 0;
        do {
            System.out.println(i);
            i++;
        } while (i < 10);
        // This is the concept related to while loop
        int j = 0;
        while (j < 10) {
            System.out.println(j);
            j++;
        }
    }
}